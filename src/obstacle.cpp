// Constructor
Obstacle::Obstacle(Vector2 position) {
    this -> position = position;

    // Iterates over the grid, first over the row, then over the columns
    for(unsigned int row = 0; row < grid.size(); ++row) {
        for(unsigned int column = 0; column < grid[0].size(); ++ column) {
            // Assigns the value to a Block if the value is 1
            if(grid[row][column] == 1) {
                // Calculates x and y coords given position in the grid - each Block is 3 pixels
                float pos_x = position.x + column * 3;
                float pos_y = position.y + row * 3;
                Block block = Block({pos_x, pos_y});
                blocks.push_back(block);
            }
        }
    }
}

// Function to draw obstacles onto screen
void Obstacle::draw() {
    // Iterate over each block in vector and draw to screen
    for(auto& block: blocks) {
        block.draw();
    }
}